    private void rentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rentActionPerformed
        // TODO add your handling code here:
        if(location > 40){
            location = location - 40;
        }
       double rents = Properties.rentAmount(location); //get rent worth
       double amount = Start.amount(player); //get how much the player has
       int owner = Properties.getOwner(location); //get the owner
       double ownerBalance = Start.amount(owner); //get the property owner balance
       ownerBalance = ownerBalance + rents; //update the balance to include the new rent
       rents = amount - rents ; //player balance after rent
       if(Properties.rent(player, location)){ //rent it
           
           Properties.update(owner, ownerBalance); //update owner info
           Properties.update(player, rents); //update info of the person who rented

            roleDice.setEnabled(true);
            rent.setEnabled(false);
            infoMessage.setText("Property rented succefully");
       }
        
    }//GEN-LAST:event_rentActionPerformed

